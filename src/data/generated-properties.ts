// 決定論的なシード付き擬似乱数
function rand(seed: number): number {
  const x = Math.sin(seed * 9301 + 49297) * 233280;
  return x - Math.floor(x);
}

function pick<T>(arr: readonly T[], seed: number): T {
  return arr[Math.floor(rand(seed) * arr.length)];
}

export function generateProperties(): Property[] {
  const properties: Property[] = [];
  let globalIdx = 0;

  for (const [code, [baseLat, baseLon]] of Object.entries(MUNICIPALITY_COORDS)) {
    const rentRange = RENT_RANGE[code];
    if (!rentRange) continue;

    const stationList = STATIONS[code] ?? ['最寄駅'];
    const count = 4 + Math.floor(rand(parseInt(code, 10)) * 4); // 4〜7件

    for (let i = 0; i < count; i++) {
      const seed = globalIdx * 17 + i;
      const room = pick(ROOM_TYPES, seed);
      const factor = ROOM_RENT_FACTOR[room];
      const baseRent = Math.round(
        (rentRange[0] + rand(seed + 1) * (rentRange[1] - rentRange[0])) * factor
      );
      // 1万円単位に丸める
      const monthlyRent = Math.round(baseRent / 10000) * 10000;

      const size = Math.round(ROOM_SIZES[room] * (0.85 + rand(seed + 2) * 0.5));
      const station = pick(stationList, seed + 3);
      const timeToStation = 2 + Math.floor(rand(seed + 4) * 18); // 2〜19分
      const buildingYear = 1985 + Math.floor(rand(seed + 5) * 40); // 1985〜2024年

      // 市区町村内で物件を散らす
      const latOffset = (rand(seed + 6) - 0.5) * 0.04;
      const lonOffset = (rand(seed + 7) - 0.5) * 0.04;

      properties.push({
        id: `gen-${code}-${i}`,
        name: `${station}駅周辺 マンション`,
        monthlyRent,
        size,
        rooms: room,
        address: `(${code.substring(0, 2) === '13' ? '東京都' : code.substring(0, 2) === '14' ? '神奈川県' : code.substring(0, 2) === '11' ? '埼玉県' : '千葉県'})`,
        latitude: baseLat + latOffset,
        longitude: baseLon + lonOffset,
        priceCategory: monthlyRent < 100000 ? 'cheap' : monthlyRent < 200000 ? 'mid' : 'expensive',
        nearestStation: station,
        timeToStation,
        buildingYear,
      });
    }
    globalIdx++;
  }

  return properties;
}
